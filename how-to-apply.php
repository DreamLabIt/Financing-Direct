               <h2>Understand Debt-to-Income</h2>
               <p>To get approved for a mortgage loan, you need to have a debt-to-income ratio that falls within the guidelines of the type of mortgage you are applying for. For example the max DTI for a FHA mortgages is 57%, and the max DTI for certain conventionals loans can be as high as 50%. </p>
               <p>To calculate your DTI ratio, first determine what your total monthly income is, including your salary and any other sources of regular monthly income. Then, consider the monthly liabilities that appear on your credit report. This includes auto loans, minimum monthly payments for outstanding credit cards and retail accounts, student loans, personal loans, etc. Certain monthly liabilities such as utilities, internet service, cell phone service, monthly subscriptions to services such as Netflix, etc do not typically appear on your credit report. </p>
               <p>Divide the total monthly liabilities that appear on your credit report by your total monthly income to get your debt-to-income ratio. This ratio will determine how much of a mortgage you qualify for.</p>
            </div>
